package main;

public class Ejercicio613 {

	public static void main(String[] args) {
		/*Ejercicio 6.1.3: Crea un programa que muestre la 
		 * "tabla de multiplicar del 5 del 0 al 10", usando "while"
		  */
		
		int numero=0;
		
		while(numero<=10) {
			System.out.println("5 x " + numero + " = " + 5*numero);
			numero++;
		}
		
		
		
	}

}
